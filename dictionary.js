function applyTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translation = translations?.[lang]?.[key];

    if (!translation) {
      console.warn(`Missing translation for: "${key}" in ${lang}`);
      return;
    }

    // Update input attributes
    if (el.hasAttribute("placeholder")) {
      el.setAttribute("placeholder", translation);
    }

    if (el.hasAttribute("title")) {
      el.setAttribute("title", translation);
    }

    // Special handling
    if (el.tagName === "OPTGROUP") {
      el.label = translation;
    } else if (el.tagName === "OPTION") {
      el.textContent = translation;
    } else if (el.tagName === "INPUT" && el.type === "submit") {
      el.value = translation;
    } else if (!el.children.length) {
      el.textContent = translation;
    }
  });
}

// Listen for language button clicks
document.querySelectorAll(".language-button").forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.dataset.lang;
    localStorage.setItem("kanbaLang", lang);
    applyTranslations(lang);

    document.querySelectorAll(".language-button").forEach((b) => b.classList.remove("selected-language"));
    btn.classList.add("selected-language");
  });
});

// Apply stored language on page load
document.addEventListener("DOMContentLoaded", () => {
  const storedLang = localStorage.getItem("kanbaLang") || "en";
  applyTranslations(storedLang);

  document.querySelectorAll(".language-button").forEach((btn) => {
    btn.classList.remove("selected-language");
    if (btn.dataset.lang === storedLang) {
      btn.classList.add("selected-language");
    }
  });
});
